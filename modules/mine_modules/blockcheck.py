def blockcheck(x):
    if x.lower() == 'stone':
        return 1
    if x.lower() == 'copper':
        return 2
    if x.lower() == 'tin':
        return 3
    if x.lower() == 'iron':
        return 4
    if x.lower() == 'aluminum':
        return 5
    if x.lower() == 'silver':
        return 6
    if x.lower() == 'topaz':
        return 7
    if x.lower() == 'gold':
        return 8
    if x.lower() == 'crystal':
        return 9
    if x.lower() == 'diamond':
        return 10
    if x.lower() == 'ruby':
        return 11
    if x.lower() == 'emerald':
        return 12