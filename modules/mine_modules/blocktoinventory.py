def blocktoinventory(p, block):
    if block == 'stone':
        p.stone += 1
    if block == 'copper':
        p.copper += 1
    if block == 'tin':
        p.tin += 1
    if block == 'iron':
        p.iron += 1
    if block == 'aluminum':
        p.aluminum += 1
    if block == 'gold':
        p.gold += 1
    if block == 'crystal':
        p.crystal += 1
    if block == 'diamond':
        p.diamond += 1
    if block == 'emerald':
        p.emerald += 1
    if block == 'topaz':
        p.topaz += 1
    if block == 'ruby':
        p.ruby += 1
    if block == 'silver':
        p.silver += 1